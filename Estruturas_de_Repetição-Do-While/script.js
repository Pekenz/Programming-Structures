//A estrutura do 'Do While' é semelhante com a do 'While', porém mudando um pouco. A diferença maior é que não é muito utilizada no dia a dia dos programadores.
//exemplo de 'Do While' abaixo:

let y = 0;

do {
    console.log(y);
    y = y + 1;
} while(y < 5);

//Resumidamente é um 'while' porém um pouco maior, onde 'while' é a condição para sair do loop.